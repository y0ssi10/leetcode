/**
 * Definition for singly-linked list.
 * class ListNode {
 *     val: number
 *     next: ListNode | null
 *     constructor(val?: number, next?: ListNode | null) {
 *         this.val = (val===undefined ? 0 : val)
 *         this.next = (next===undefined ? null : next)
 *     }
 * }
 */

function reverseList(head: ListNode | null): ListNode | null {
  let dummy = null;
  let node = head;
  while (node !== null) {
    const curr = node;
    node = node.next;
    curr.next = dummy;
    dummy = curr;
  }

  return dummy;
};
